"""
텍스트 정리 유틸리티.

Ollama 응답 또는 추출된 텍스트의 불필요한 공백·줄바꿈을 제거한다.
마크다운 구조는 보존하면서 3줄 이상의 연속 빈 줄만 압축한다.
"""
import re


def clean(text: str) -> str:
    """
    텍스트의 과도한 공백과 줄바꿈을 정리한다.

    - 3개 이상 연속된 줄바꿈 → 2개로 압축 (마크다운 단락 구분 유지)
    - 같은 줄에서 2개 이상 연속된 공백·탭 → 1개로 압축
    - 앞뒤 공백 제거

    Args:
        text: 정리할 원본 문자열
    Returns:
        정리된 문자열
    """
    text = re.sub(r'\n{3,}', '\n\n', text)    # 빈 줄 3개 이상 → 2개
    text = re.sub(r'[ \t]{2,}', ' ', text)    # 연속 공백·탭 → 단일 공백
    return text.strip()
